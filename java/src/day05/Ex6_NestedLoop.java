package day05;

public class Ex6_NestedLoop {

	public static void main(String[] args) {

		/* ******
		 * ******
		 * ******
		 * ******
		 * ******
		 * 
		 */
		for (int i = 1; i <= 5; i++) {
			// 별 5개 출력하는 코드. => * 10개 출력하는 코드로 바꾸자.
			for (int j = 1; j <= 5; j++) {
				System.out.print("*");

			}

			System.out.println();
		}

	}

}
